import { describe, test, expect, afterAll } from "bun:test";
import { readFileSync, writeFileSync, mkdirSync, rmSync } from "node:fs";
import { join } from "node:path";
import { execSync } from "node:child_process";
import { PROJECT_ROOT, makeTempDir } from "../src/test-helpers.js";

describe("generate-prompts.ts", () => {
  test("produces src/embedded-prompts.ts with the expected fragment count", () => {
    execSync("bun scripts/generate-prompts.ts", {
      cwd: PROJECT_ROOT,
      encoding: "utf8",
      timeout: 10000,
      stdio: ["pipe", "pipe", "pipe"],
    });
    const content = readFileSync(
      join(PROJECT_ROOT, "src/embedded-prompts.ts"),
      "utf8",
    );
    expect(content).toContain("EMBEDDED_PROMPTS");
    // Count fragment entries (lines with "path": ` pattern) — includes .md and .json files
    const entries = content.match(/^\s+"(?:base|chill|axis|modifiers)\/[a-z/\-.]+": `/gm);
    expect(entries).toHaveLength(33);
  });

  test("generated file has auto-generated header comment", () => {
    const content = readFileSync(
      join(PROJECT_ROOT, "src/embedded-prompts.ts"),
      "utf8",
    );
    expect(content.startsWith("// Auto-generated by scripts/generate-prompts.ts")).toBe(true);
    expect(content).toContain("DO NOT EDIT");
  });

  test("generated file is valid TypeScript that exports EMBEDDED_PROMPTS", () => {
    // Import the generated module — if it's invalid TS, this will throw
    const mod = require(join(PROJECT_ROOT, "src/embedded-prompts.ts"));
    expect(mod.EMBEDDED_PROMPTS).toBeDefined();
    expect(typeof mod.EMBEDDED_PROMPTS).toBe("object");
  });

  test("generated content preserves special characters correctly", () => {
    // Verify a fragment with template variables (env.md has {{CWD}} etc.)
    const mod = require(join(PROJECT_ROOT, "src/embedded-prompts.ts"));
    const envContent = mod.EMBEDDED_PROMPTS["base/env.md"];
    expect(envContent).toContain("{{CWD}}");
    expect(envContent).toContain("{{PLATFORM}}");
  });
});

describe("generate-prompts.ts error handling", () => {
  let tempDir: string;

  afterAll(() => {
    if (tempDir) rmSync(tempDir, { recursive: true, force: true });
  });

  test("fails with clear error when a fragment file is missing", () => {
    // Create a temp copy of the script that points to a directory missing a file
    tempDir = makeTempDir("gen-prompts-err-");
    const promptsDir = join(tempDir, "prompts");
    mkdirSync(join(promptsDir, "base"), { recursive: true });
    // Only create one file — the script expects 20
    writeFileSync(join(promptsDir, "base", "intro.md"), "test", "utf8");

    // Create a modified script that uses our temp prompts dir
    const scriptContent = readFileSync(
      join(PROJECT_ROOT, "scripts/generate-prompts.ts"),
      "utf8",
    )
      .replace(
        /const PROMPTS_DIR = .+/,
        `const PROMPTS_DIR = "${promptsDir}";`,
      )
      .replace(
        /const OUTPUT_FILE = .+/,
        `const OUTPUT_FILE = "${join(tempDir, "output.ts")}";`,
      );
    const modifiedScript = join(tempDir, "generate.ts");
    writeFileSync(modifiedScript, scriptContent, "utf8");

    try {
      execSync(`bun run ${modifiedScript}`, {
        encoding: "utf8",
        timeout: 10000,
        stdio: ["pipe", "pipe", "pipe"],
      });
      throw new Error("Expected script to fail");
    } catch (err: any) {
      const output = (err.stderr || err.message || "").toString();
      expect(output).toContain("Missing prompt fragment");
    }
  });
});
