#!/usr/bin/env bun
/**
 * Reads all built-in prompt fragments from prompts/ and generates
 * src/embedded-prompts.ts — a Record<string, string> keyed by relative path.
 *
 * Run: bun scripts/generate-prompts.ts
 */

import { readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const PROJECT_ROOT = join(import.meta.dir, "..");
const PROMPTS_DIR = join(PROJECT_ROOT, "prompts");
const OUTPUT_FILE = join(PROJECT_ROOT, "src", "embedded-prompts.ts");

const FRAGMENT_PATHS = [
  // Standard base fragments
  "base/intro.md",
  "base/system.md",
  "base/doing-tasks.md",
  "base/actions.md",
  "base/tools.md",
  "base/tone.md",
  "base/text-output.md",
  "base/session-guidance.md",
  "base/env.md",
  // Standard base manifest
  "base/base.json",
  // Chill base manifest and fragments
  "chill/base.json",
  "chill/core.md",
  "chill/actions.md",
  "chill/tools.md",
  "chill/env.md",
  // Axis fragments
  "axis/agency/autonomous.md",
  "axis/agency/collaborative.md",
  "axis/agency/surgical.md",
  "axis/agency/partner.md",
  "axis/quality/architect.md",
  "axis/quality/pragmatic.md",
  "axis/quality/minimal.md",
  "axis/scope/unrestricted.md",
  "axis/scope/adjacent.md",
  "axis/scope/narrow.md",
  // Modifiers
  "modifiers/readonly.md",
  "modifiers/context-pacing.md",
  "modifiers/debug.md",
  "modifiers/methodical.md",
  "modifiers/director.md",
  "modifiers/bold.md",
  "modifiers/speak-plain.md",
  "modifiers/tdd.md",
] as const;

function escapeTemplateLiteral(content: string): string {
  return content
    .replace(/\\/g, "\\\\")
    .replace(/`/g, "\\`")
    .replace(/\$\{/g, "\\${");
}

function generateEmbeddedPrompts(): void {
  const entries: string[] = [];

  for (const relativePath of FRAGMENT_PATHS) {
    const fullPath = join(PROMPTS_DIR, relativePath);
    let content: string;
    try {
      content = readFileSync(fullPath, "utf8");
    } catch {
      throw new Error(
        `Missing prompt fragment: ${relativePath} (expected at ${fullPath})`
      );
    }
    const escaped = escapeTemplateLiteral(content);
    entries.push(`  "${relativePath}": \`${escaped}\``);
  }

  const output = `// Auto-generated by scripts/generate-prompts.ts — DO NOT EDIT
// Regenerate: bun scripts/generate-prompts.ts

/** Built-in prompt fragments keyed by relative path (e.g. "base/intro.md") */
export const EMBEDDED_PROMPTS: Record<string, string> = {
${entries.join(",\n")},
};
`;

  writeFileSync(OUTPUT_FILE, output, "utf8");
  process.stdout.write(`Generated ${OUTPUT_FILE} with ${FRAGMENT_PATHS.length} fragments\n`);
}

generateEmbeddedPrompts();
